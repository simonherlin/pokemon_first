def grid_to_image(grid: list, width: int = 32, height: int = 64) -> Image.Image:
    """Convertit une grille de caractères en image PIL 32×64."""
    img = Image.new("RGBA", (width, height), T)
    for y, line in enumerate(grid):
        line = line.ljust(width, '.')
        if y >= height:
            break
        for x, ch in enumerate(line):
            if x >= width:
                break
            color = CHAR_MAP.get(ch, T)
            if color != T:
                img.putpixel((x, y), color)
    return img


def mirror_image(img: Image.Image) -> Image.Image:
    """Miroir horizontal d'une image."""
    return img.transpose(Image.FLIP_LEFT_RIGHT)


